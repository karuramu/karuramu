package javaExamplePrograms;

public class AutoTypeCasting {
	public static void main(String[] args) {
		
		
		float f;
		
		byte b = 10;
		short s = 100;
		int i = 1000;
		long l = 999999999L;
		float ff = 123.89F;
		char c = 'A';
		
		f = b;
		System.out.println(f);
		
		f = s;
		System.out.println(f);
		
		f =i;
		System.out.println(f);
		
		f = l;
		System.out.println(f);
		
		f = ff;
		System.out.println(f);
		
		f = c;
		System.out.println(f);
		
		
		
		
		
		
	}

}
