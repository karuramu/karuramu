package javaExamplePrograms;

public class TernaryOperator {
	
	public static void main(String[] args) {
		int x =2, y=1;
		int result;
		 result = (x>y)?x:y; //ternary operation
		 System.out.println(result);
		 
		 System.out.println("===============");
		 
		 result = (x>y)?x*10:y*6; //ternary operation
		 System.out.println(result);
		 
	}

}
